  /** Rich bilingual invoice description from contract + property context */
  buildInvoiceDescription(inv, client, prop, contract) {
    const custom = String(inv?.description || '').trim();
    // Always honor custom invoice text (except generic defaults).
    if (custom && !/^(rent invoice|فاتورة إيجار)$/i.test(custom)) {
      return { ar: custom, en: custom, period: this.invoicePeriodLabel(inv, contract) };
    }
    const aptMatch = String(prop?.name || '').match(/شقة\s*(\d+)/);
    const aptNo = aptMatch ? aptMatch[1] : (String(contract?.unit_details || '').match(/\d+/) || [''])[0];
    const unit = prop?.name || contract?.unit_details || 'الوحدة المؤجرة';
    const location = prop?.location || 'نزوى — حي التراث';
    const tenant = client?.name || 'المستأجر';
    const period = this.invoicePeriodLabel(inv, contract);
    const cycle = String(contract?.payment_cycle || 'monthly').toLowerCase();
    const cycleAr = cycle.includes('year') ? 'سنوي' : cycle.includes('quarter') ? 'ربع سنوي' : 'شهري';
    const cycleEn = cycle.includes('year') ? 'Annual' : cycle.includes('quarter') ? 'Quarterly' : 'Monthly';
    return {
      ar: `إيجار ${cycleAr} — ${unit}${aptNo ? ` (شقة ${aptNo})` : ''} — ${location}\nالمستأجر: ${tenant}\n${period.ar}`,
      en: `${cycleEn} rent — ${unit}${aptNo ? ` (Apt ${aptNo})` : ''} — ${location}\nTenant: ${tenant}\n${period.en}`,
      period,
    };
  },

  invoicePeriodLabel(inv, contract) {
    const todayIso = () => new Date().toISOString().slice(0, 10);
    const parse = (s) => {
      if (!s) return null;
      const d = new Date(String(s) + 'T00:00:00');
      return Number.isNaN(d.getTime()) ? null : d;
    };
    const fmt = x => x.toISOString().slice(0, 10);
    const fmtAr = x => x.toLocaleDateString('ar-OM');
    const contractStart = parse(contract?.start_date);
    const contractEnd = parse(contract?.end_date);
    if (contractStart && contractEnd && contractStart <= contractEnd) {
      const diffDays = Math.floor((contractEnd - contractStart) / 86400000) + 1;
      // Short contracts should display their exact contractual period.
      if (diffDays <= 35 || /short/i.test(String(contract?.contract_type || ''))) {
        return {
          from: fmt(contractStart),
          to: fmt(contractEnd),
          ar: `فترة الفاتورة: ${fmtAr(contractStart)} — ${fmtAr(contractEnd)}`,
          en: `Billing period: ${fmt(contractStart)} — ${fmt(contractEnd)}`,
        };
      }
    }
    const due = inv?.due_date || inv?.issue_date || todayIso();
    const d = parse(due);
    if (!d) return { ar: 'الفترة: —', en: 'Period: —', from: due, to: due };
    const from = new Date(d.getFullYear(), d.getMonth(), 1);
    const to = new Date(d.getFullYear(), d.getMonth() + 1, 0);
    return {
      from: fmt(from),
      to: fmt(to),
      ar: `فترة الفاتورة: ${fmtAr(from)} — ${fmtAr(to)}`,
      en: `Billing period: ${fmt(from)} — ${fmt(to)}`,
    };
  },

  vatBreakdown(grossAmount) {
    const rate = Number(this.settings.vat_rate ?? 0.05);
    const gross = Number(grossAmount || 0);
    if (!rate || rate <= 0) return { subtotal: gross, vat: 0, total: gross, rate: 0 };
    const subtotal = gross / (1 + rate);
    const vat = gross - subtotal;
    return { subtotal, vat, total: gross, rate };
  },

  escapeHtml(v) {
    return String(v ?? '').replace(/[&<>"']/g, ch => ({ '&': '&amp;', '<': '&lt;', '>': '&gt;', '"': '&quot;', "'": '&#39;' }[ch]));
  },

  defaultLegalTerms() {
    return window.DEFAULT_LEGAL_TERMS;
  },

  formatLegalTermsHtml(text) {
    const src = String(text || this.defaultLegalTerms()).trim();
    const sectionTitles = new Set(['الشروط القانونية:', 'ملاحظات التسليم:', 'إنهاء العقد:', 'ملاحظة:']);
    const blocks = [];
    let title = 'الشروط القانونية';
    let lines = [];
    const flush = () => {
      if (!lines.length) return;
      blocks.push(`<div class="qls-card qls-legal-section"><h4>${this.escapeHtml(title)}</h4><p>${lines.map(x => this.escapeHtml(x)).join('<br>')}</p></div>`);
      lines = [];
    };
    src.split('\n').forEach(raw => {
      const line = raw.trim();
      if (!line) return;
      if (sectionTitles.has(line)) {
        flush();
        title = line.slice(0, -1);
        return;
      }
      lines.push(line);
    });
    flush();
    return blocks.join('');
  },

